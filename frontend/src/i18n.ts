export function getLocale(): Locale {
  return currentLocale
}

export function setLocale(loc: Locale): void {
  currentLocale = loc
  if (typeof document !== 'undefined') {
    document.documentElement.lang = loc === 'he' ? 'he' : 'en'
    document.documentElement.dir = loc === 'he' ? 'rtl' : 'ltr'
  }
}

/** Translate key for current locale. */
export function t(key: string): string {
  const map = translations[currentLocale]
  return (map && map[key]) || translations.en[key] || key
}

/** Status label (values may be API en; we show localized). */
export function statusLabel(status: string): string {
  const key = status === 'pending_review' ? 'pendingReview' : status === 'approved' || status === 'final' ? 'approved' : 'rejected'
  return t(key)
}
